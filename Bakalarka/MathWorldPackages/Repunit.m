Begin["`Private`"]

Repunit[n_Integer?Positive,b_Integer:10]:=(b^n-1)/(b-1)

RepunitCheck[{n1_,n2_},opts___]:=RepunitCheck[{n1,n2,1},opts]

RepunitCheck[{n1_,n2_,step_},opts___]:=Module[{i,r,f,f1},
	Do[
		If[RepunitFactors[i]=={},
			Print[i,". ",RepunitAnalyticFactor[i]],
			If[Times@@RepunitFactors[i]==Repunit[i],
				If[Union[PrimeQ[RepunitFactors[i]]]=={True},
					Print[i,". factored into primes"],
					Print[i,". factors are not all primes!"]],
			f1=RepunitAnalyticFactor[i,opts];
			r=Union[Complement[f1,RepunitFactors[i],Select[f1,!PrimeQ[#]&]]];
			f=FactorInteger[Repunit[i]/Times@@r/Times@@RepunitFactors[i],opts];
			If[f=={},Print[i,". ",r],Print[i,". ",r," ",Transpose[f]]]]
		],
		{i,n1,n2,step}
	]
]

RepunitAnalyticFactor[n_,opts___]:=Module[{afactors,b=10},
	afactors=Drop[FactorList[(b0^n-1)/(b0-1)],1]; 
	afactors=afactors /. b0->b;
	Transpose[Flatten[FactorInteger[Transpose[afactors][[1]],opts],1]][[1]]
]

