BouwkampConstruction[l_]:=Module[{n=Plus@@First[l],a,boxes,allboxes={}},
    a=Table[0,{n},{n}];
    Do[
      p=Reverse[Position[a,0,2,1][[1]]];
      AppendTo[allboxes,
        boxes={{First[#],p[[2]]},{Last[#],p[[2]]-Subtract@@#}}&/@
            Partition[FoldList[Plus,p[[1]],l[[k]]],2,1]];
      Do[a[[j,i]]=1,{i,#[[1,1]],#[[2,1]]-1},{j,#[[1,2]],#[[2,2]]-1}]&/@boxes,
      {k,Length[l]}
    ];
    allboxes-1
]

BouwkampQ[l_]:=Module[{n=Plus@@First[l],a,boxes},
    a=Table[0,{n},{n}];
    Do[
      p=Reverse[Position[a,0,2,1][[1]]];
        boxes={{First[#],p[[2]]},{Last[#],p[[2]]-Subtract@@#}}&/@
            Partition[FoldList[Plus,p[[1]],l[[k]]],2,1];
      Do[a[[j,i]]=1,{i,#[[1,1]],#[[2,1]]-1},{j,#[[1,2]],#[[2,2]]-1}]&/@boxes,
      {k,Length[l]}
    ];
    Position[a,0,{2},1]=={}
]


