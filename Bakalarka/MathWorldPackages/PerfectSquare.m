EllBuild[{grow_List,square_List},rot_,offset_List:{0,0}]:=Module[
  {
    i,q,
    rectgrow=grow,rectsquare=square,
    x0=offset[[1]],y0=offset[[2]],x,y,dx,dy,
    hflip=1,vflip=1,newhflip=1,newvflip=1,
    side,squarelist={},textlist={}
  },
  
  (* Set Initial Parameters *)
  
  x=rectsquare[[2]]; y=x; dx=rectsquare[[1]]; dy=dx;
  rectsquare=Drop[rectsquare,2];
  squarelist={Square[{x0,y0},x,{1,1},rot],Square[{x0+x,y0},dy,{1,1},rot]};
  textlist={Text[ToString[x],Rot[{x0+x/2,y0+x/2},rot]],
    Text[ToString[dy],Rot[{x0+x+dy/2,y0+dy/2},rot]]};
  
  Do[
    side=rectsquare[[i]];
    Switch[rectgrow[[i]],
      0,q={x0-hflip side,y0};x+=side;x0-=hflip side,
      1,q={x0,y0+vflip y};y+=side,
      2,q={x0+hflip x,y0+vflip dy};dx-=side;x+=side,
      3,q={x0+hflip x,y0+vflip dy};dy+=side,
      4,q={x0+hflip(x+dx),y0};dx+=side,
      5,q={x0,y0-vflip side};y0-=side;dy+=side;y+=side,
      6,q={x0+hflip x,y0+vflip dy};newvflip*=-1;
        x+=dx; dx=side-dx; y0+=vflip y; dy=side,
      7,q={x0+hflip x,y0+vflip dy};newhflip*=-1;
        temp=y; y=dy+side; dy=temp; x0+=hflip(x+dx); dx=x; x=side,
      True,Message[EllBuild::unknown,rectgrow[[i]]]
    ];
(*
    Print[i,". ",rectgrow[[i]]," ",side," ",{hflip,vflip}," ",
      {x0,y0}," ",{x,y}," ",{dx,dy}]; 
*)
    squarelist=Append[squarelist,Square[q,side,{hflip,vflip},rot]];
    textlist=Append[textlist,
      Text[ToString[side],Rot[{q[[1]]+hflip side/2,q[[2]]+vflip side/2},rot]]];
    hflip=newhflip; vflip=newvflip,
    {i,Length[rectgrow]}
  ];
  
  (* Finish Rectangle *)
  
  If[Length[rectsquare]>Length[rectgrow],
    side=rectsquare[[-1]];
    squarelist=Append[squarelist,
      Square[{x0+hflip x,y0+vflip dy},side,{hflip,vflip},rot]];
    textlist=Append[textlist,Text[ToString[side],
      Rot[{x0+hflip(x+side/2),y0+vflip(dy+side/2)},rot]]];
    x+=side;
  ];
  Print["Done: ",
      {x0,y0}," ",{x,y}," ",{dx,dy}];
  {squarelist, textlist}
]

Squares[l_,opts___]:=Module[{labeled,color},
	{labeled,minlabel,color}={Labeled,MinLabel,Color}/.{opts}/.Options[Squares];
	If[!labeled,minlabel=Infinity];
	If[color===None,
		Map[{SquareEdges[#],SquareLabel[#,minlabel]}&,l,{2}],
	(* Else *)
		Map[{
			{color,Rectangle[{#[[1,1]],-#[[1,2]]},{#[[2,1]],-#[[2,2]]}]},
        		SquareLabel[#,minlabel],SquareEdges[#]}&,l,{2}]
	]
]

Rot[{x_,y_},0]:={x,y}
Rot[{x_,y_},1]:={y,-x}

SideLength[square_List]:=Plus@@square[[1]]

Square[{x_,y_},size_,{hflip_,vflip_},0]:=
  Line[{{x,y},{x+hflip size,y},{x+hflip size,y+vflip size},{x,y+vflip size},{x,y}}]

Square[{x_,y_},size_,{hflip_,vflip_},1]:=
  Line[{{y,-x},{y+vflip size,-x},{y+vflip size,-(x+hflip size)},
    {y,-(x+hflip size)},{y,-x}}] 

Square[{x_,y_},size_]:=Line[{{x,y},{x+size,y},{x+size,y-size},{x,y-size},{x,y}}]

SquareEdges[{{x1_,y1_},{x2_,y2_}}]:=
  Line[{{x1,-y1},{x2,-y1},{x2,-y2},{x1,-y2},{x1,-y1}}]

SquareLabel[l_,min_:0]:=Module[{num=-Subtract@@(First/@l)},
	If[num>=min,
		Text[num,{First[#],-Last[#]}&[Plus@@@Transpose[l]/2]],
		Sequence@@{}
	]
]


