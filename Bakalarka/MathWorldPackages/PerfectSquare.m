(*:Discussion:

See http://mathworld.wolfram.com/PerfectSquareDissection.html

The "ell" method is due to D. Sleator.  Here are his instructions on his
notation for perfect squares.

Consider the square of size  175 made of  24 squares with parameters given by

  rect grow sequence =           2   1   0   1   6   7   7   0   4   7
  rect squares       =   3   4   1   5   9  14  20  18  16  39  38  56  55
  ell  grow sequence =           2   1   0   6   3   4   7   2   1
  ell  squares       =  29  31   2  33  64  35   8  43  51  30  81

The key is in the grow sequence.  There's an ell shape with 6 sides
numbered 0-5.

                 1
	+-----------------+
	|                 |
	|                 | 2
	|                 |     3
	|                 +----------+
  0	|                            |
	|                            |
	|                            | 4
	|                            |
	|                            |
	+----------------------------+
                       5

The first two squares are glued together to make the first ell.  It's
grown by adding new squares onto one of the 6 sides of the ell.  You add
the square with side length that matches the side you're adding it on.
The number in the grow sequence tells you which side to add onto.
(Numbers 6 and 7 are an alternate indication for sides 2 and 3,
respectively.)

Each subsequent square is added onto the appropriate side.  In case 6
the ell is then flipped along a horizontal axis, and in case 7 it is
flipped along a vertical axis (to preserve the orientation above).

Now after you've built the rect grow sequence into a large ell, you plug
in the corner of the resulting ell with the last extra square at the end
of the square list.  This forms a rectangle.  Then you grow a new ell
using the ell grow sequence, then you plug the rectangle into the hole
in that ell, and you've got a square.

*)

