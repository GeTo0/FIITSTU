(*:Limitations: 
  Expressions (k1|k2|...,m,n) are evaluated as if they are (k,m,n) if
    (k,m,n) is defined and k is one of k1, k2, ...  This is because I
    don't know how to pull out a value from an expression of the form
    a[1|2,4]:=1234 if a[1,4] is already defined.
  Some, but not all, expressions of the form i*j have been expanded
  Not all lists are given in correct lexographic order
  Multigrade equations are listed under first order only (using Alternatives)
  Only positive integer identities included
*)

