LikePowers::usage =
"LikePowers[k,m,n] gives a list of known identities of sums of \
m and n integers to the kth power."

LikePowersCheck::usage =
"LikePowersCheck[k,m1,m2,...] returns the differences of the left-hand and \
right-hand sides of the identities given by LikePowers[k,m1,m2,...].  Any \
nonzero entries are erroneous  LikePowersCheck[k] checks all identities
of order k.  LikePowersCheck[] checks all identities."

LikePowersExpand::usage =
"LikePowersExpand[k,m1,m2,...] expands the identity."


Begin["`Private`"]

LikePowers::ParametricSolution =
"A parametric solution is known for this type of equation."

LikePowers::NoSolution =
"There is no solution to this type of equation."

LikePowers::Inconsistent =
"Those statements can't both be true!"

LikePowersCheck::Error =
"This identity is in error."


LikePowersCheck[]:=Module[{d=DownValues[LikePowers],max,i},
	(*
	 first argument of LikePowers[...] = the order
	*)
	max=Max[Flatten[(#[[1,1,1]]&/@ DownValues[LikePowers])/.Alternatives->List]];
	Do[
		LikePowersCheck[i],
		{i,max}
	]
]

LikePowersCheck[k_Integer]:=Module[{d,l,i,args},
	d=DownValues[LikePowers];
	(*
	 create a list of ... for all defined LikePowers[...]
	*)
	l=Select[Table[(d[[i,1]] /. LikePowers->List)[[1]],{i,Length[d]}],MatchQ[k,#[[1]]]&];
	Print["Length ",k,": ",Length[l]," out of ",Length[d]," identities."];
	Do[
		(*
		 take the order of interest with the corresponding identity
		*)
		args=Join[{k},Drop[l[[i]],1]];
		Print[l[[i]],"->",args];
		Print[LikePowersCheck[Sequence@@args]],
		{i,Length[l]}
	]
]

LikePowersCheck[l__]:=Module[{ok},
	If[LikePowersParametric[l],Message[LikePowers::ParametricSolution]];
	If[LikePowersNoSolution[l],Message[LikePowers::NoSolution]];
	If[LikePowersParametric[l] && LikePowersNoSolution[l],
		Message[LikePowers::Inconsistent]];
	If[ValueQ[LikePowers[l]],
		ok=Flatten[SubtractFirst/@LikePowersExpand[l]];
		If[Union[ok]!={0},Message[LikePowersCheck::Error]];
		ok
	]
]

SubtractFirst[l_List]:=Drop[l,1]-l[[1]]

LikePowersExpand[l__]:=Apply[Plus,LikePowers[l]^({l}[[1]]),{2}]

